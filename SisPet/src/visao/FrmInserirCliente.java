    public class EstadosCellRenderer extends DefaultListCellRenderer {

        @Override
        public Component getListCellRendererComponent(JList<? extends Object> list,
                Object value, int index, boolean isSelected, boolean cellHasFocus) {
            super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);

            if (value instanceof EstadoModelo) {
                EstadoModelo estado = (EstadoModelo) value;
                setText(estado.getSigla() + " - " + estado.getNome());
            }
            return this;
        }
    }

    public class CidadesCellRenderer extends DefaultListCellRenderer {

        @Override
        public Component getListCellRendererComponent(JList<? extends Object> list,
                Object value, int index, boolean isSelected, boolean cellHasFocus) {
            super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);

            if (value instanceof CidadeModelo) {
                CidadeModelo cidade = (CidadeModelo) value;
                setText(cidade.getNome());
            }
            return this;
        }
    }

