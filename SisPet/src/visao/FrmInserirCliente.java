    private void jButton_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SalvarActionPerformed
        if ((!jTextField_Nome.getText().equals(""))
                && (!jFormattedTextField_Cpf.getText().equals("   .   .   -  "))
                && (jComboBox_Cidades.isEnabled())) {

            ArrayList<String> Registro = new ArrayList<String>();
            Registro.add(jTextField_Codigo.getText());
            Registro.add(jTextField_Nome.getText());
            Registro.add(jFormattedTextField_Cpf.getText());
            Registro.add(jFormattedTextField_Cep.getText());
            Registro.add(jTextField_Endereco.getText());
            Registro.add(jTextField_Bairro.getText());
            Registro.add(jTextField_Numero.getText());
            Registro.add(jTextField_Complemento.getText());
            Registro.add(jFormattedTextField_Telefone.getText());
            Registro.add(jFormattedTextField_Celular.getText());

            CidadeModelo cidade = (CidadeModelo) jComboBox_Cidades.getModel().getSelectedItem();
            try {
                Registro.add(String.valueOf(cidade.getCodigo()));
            } catch (Exception e) {
                Registro.add("");
            }

            Registro.add(usuarioLogado);

            ClienteControle ControleCliente = new ClienteControle();

            int idCodNovo = ControleCliente.Salvar(Registro);
            jTextField_Codigo.setText(String.valueOf(idCodNovo));
            JOptionPane.showMessageDialog(null, "Salvo com sucesso");

            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Preencha o Nome, CPF e escolha uma cidade do cliente");
        }
    }//GEN-LAST:event_jButton_SalvarActionPerformed

