    private void jComboBox_EstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox_EstadoActionPerformed
        if (!jComboBox_Cidades.isEnabled()) {
            jComboBox_Cidades.setEnabled(true);
        }
        EstadoModelo estado = (EstadoModelo) jComboBox_Estado.getModel().getSelectedItem();
        String codigo = String.valueOf(estado.getCodigo());

        List<CidadeModelo> listaCidades = new CidadeControle().criaListCidades(codigo);
        ComboCidadeModelo modelCidades = new ComboCidadeModelo(listaCidades);
        jComboBox_Cidades.setModel(modelCidades);
        jComboBox_Cidades.setRenderer(new CidadesCellRenderer());

    }//GEN-LAST:event_jComboBox_EstadoActionPerformed

